package anoud;

public class PramidReverse {

	public static void main(String[] args) {
		int N=3; 
	    for(int i=1;i<=N;i++) 
	      {  
	        for(int j=N;j>=i;j--) 
	        { 
	          System.out.print("#### "); 
	        } 
	        System.out.println(); 
	      }

	}

}
